    <span style="font-family: Lucida Bright; color: #a8a8a8;">El río Sabinal
      nace en una cueva del municipio de&nbsp; Berriozabal y baja por una cañada
      donde forma pozas de agua limpia, <br>
      su camino es nutrido por 21 diferentes afluentes, atraviesa el norte de la
      ciudad y desemboca en el río Grijalva, algunos afluentes <br>
      que lo nutren son Arroyo la Chacona, el Poti, San Roque y algunos
      embovedados que vierten sus aguas en el río de la ciudad.<br>

    <span style="font-family: Lucida Bright; color: #a8a8a8;">En 1892 se hizo un
      plano de Tuxtla Gutiérrez en el que se puede apreciar una población muy
      pequeña. <br>
      Las ultimas calles de este a oeste eran la 9a. Oriente y la 9a. Poniente.
      Por el lado sur las últimas casas llegaban hasta la 9a Sur <br>
      y por el norte no se podía ampliar debido a que ahí se encontraba el Río
      Sabinal.<br>
      El río Sabinal es una afluente sobre el cual convergen diversos arroyos
      tributarios de la capital del estado, y es además un espacio
      representativo de la capital del estado.<br>
      Su nombre proviene de la voz popular que lo nombraba por el amplio número
      de sabinos que podían verse en los márgenes del entonces cristalino río.<br>
      Sin embargo, debido a la falta de planeamiento en el crecimiento
      demográfico de la ciudad, aunado a la falta de cultura ambiental<br>
      y algunos fenómenos naturales, su cause contamina y sus márgenes reducidos
      perdieron inercia natural, provocando eventuales afectaciones a las <br>
      viviendas, comercios locales y población en generál.<br>
      En algún momento se planeó un proyecto de rescate a sus cauces que nunca
      se complementó, pero hoy en día está abandonado a la suerte que le<br>
      dicta el crecimiento anárquico en la ciudad.<br>
      El río Sabinal tiene una longitud aproximada de 13 mil 445.82 metros, se
      denomina así por alojar a lo largo de su cauce una población importante<br>
      de sabinos (Taxodium mucronatum, Ahuehuete, Ciprés calvo, Ciprés mexicano
      y/o Ciprés de Moctezuma).<br>
      Así, el río Sabinal es un elemento ligado al desarrollo de la capital del
      estado, la ciudad de Tuxtla Gutiérrez. Sin embargo, por la falta<br>
      de planeación y la invasión del límite de la zona federal, cuando se han
      presentado fenómenos hidrometereológicos extraordinarios, ha provocado<br>
      inundaciones con cuantiosas pérdidas económicas e incluso algunas pérdidas
      humanas, los más recientes en 1996 y 1998.</span><br>

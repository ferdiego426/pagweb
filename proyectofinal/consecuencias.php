      <h4>
        <br>
        <br>
        <br>
        <span style="font-family: Lucida Bright; color: #a3a3a3;">En cuanto al
          mal olor que tiene el río Sabinal en su periferia, se debe al bajo
          registro de niveles de oxígeno disuelto, lo que detiene <br>
          el proceso aeróbico del agua para dar paso al proceso anaeróbico, lo
          que hace que el agua respire de manera lenta y mal oliente.
          <br>
          <br>
          Por otro lado el agua contaminada transporta un número incalculable de
          bacterias, además de gusanos, lombrices y virus.
          <br>
          De estar en contacto y/o ser consumida por el ser humano, puede
          desencadenar enfermedades como fiebre tifoidea, disentería, cólera,
          entre otros.
          <br>
          <br>
          Ante el incremento de enfermedades relacionadas con la calidad del
          agua, darle la importancia requerida al tema de la contaminación del
          <br>
          río Sabinal, pues más allá de ofrecer un mal aspecto de la ciudad,
          forma es un peligro latente para la salud y el ambiente.
